--1.新建表-PERF_T_ADJUSTREMIND
i INTEGER;
BEGIN
	SELECT count( * ) INTO i FROM user_tables t WHERE t.TABLE_NAME = 'PERF_T_ADJUSTREMIND';
	IF i = 0 THEN execute IMMEDIATE '
			CREATE TABLE PERF_T_ADJUSTREMIND
			(	GUID VARCHAR2(40) NOT NULL ENABLE,
				PROJGUID VARCHAR2(40) NOT NULL ENABLE,
				MAINGUID VARCHAR2(40) NOT NULL ENABLE,
				STATUS NUMBER(1,0),
				CONFIRMTIME VARCHAR2(17),
				AGENCYGUID VARCHAR2(40),
				FININTORGGUID VARCHAR2(40),
				PROVINCE VARCHAR2(32) NOT NULL ENABLE,
				YEAR NUMBER(4,0) NOT NULL ENABLE,
				CREATER VARCHAR2(40),
				CREATETIME VARCHAR2(17),
				UPDATETIME VARCHAR2(17),
				LASTUPDATETIME VARCHAR2(17)
			)';
  execute IMMEDIATE 'ALTER TABLE PERF_T_ADJUSTREMIND ADD CONSTRAINT PK_PERF_T_ADJUSTREMIND PRIMARY KEY (GUID, YEAR, PROVINCE)';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.GUID IS ''guid''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.PROJGUID IS ''项目id''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.MAINGUID IS ''关联id(目标调整id)''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.STATUS IS ''状态(是否确认 1确认 0未确认)''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.CONFIRMTIME IS ''确认时间''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.AGENCYGUID IS ''单位id''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.FININTORGGUID IS ''处室id''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.PROVINCE IS ''区划''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.YEAR IS ''年度''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.CREATER IS ''创建人''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.CREATETIME IS ''创建时间''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.UPDATETIME IS ''更新时间''';
   execute IMMEDIATE 'COMMENT ON COLUMN PERF_T_ADJUSTREMIND.LASTUPDATETIME IS ''最后更新时间''';
END IF;

execute immediate '
CREATE OR REPLACE VIEW v_perf_t_adjustremind AS 
SELECT * 
FROM PERF_T_ADJUSTREMIND WHERE year = global_multyear_cz.Secu_f_GLOBAL_PARM ( ''YEAR'' ) AND province = global_multyear_cz.Secu_f_GLOBAL_PARM ( ''DIVID'' )';
